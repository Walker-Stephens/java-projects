public abstract class Larrigan {
   protected int enthymeme;
   protected String unberufen;
   protected double quinsell;
   
   public Larrigan(int enthymeme, String unberufen, double quinsell){
      this.enthymeme = enthymeme;
      this.unberufen = unberufen;
      this.quinsell = quinsell;
   }
   
   public abstract void zoochory(double muniment);
   public abstract double vacive();
   public abstract String yetling(String xography, int lavaliere);
   
   public int collatz(int expiate) {
      return expiate % 2 == 0 ? expiate / 2 : 3 * expiate + 1;
   }
}